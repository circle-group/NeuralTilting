"""
Model Comparison Visualization Script

Loads evaluation results from multiple runs and creates comparative visualizations.

Evaluation results are saved by evaluate_runs.py under a hierarchical path:
    evaluation/results/{model_type}/alpha_{alpha:.2f}_obsstd_{obs_std:.2f}/
        {run_id}_gt_{ground_truth}_obs_{eval_obs}_sim_{sim_grid}/

Use --eval-dirs to pass the full result directories directly.
--run-ids will NOT work because the directory name encodes the evaluation
options (ground_truth, eval_obs, sim_grid) appended after the run_id.

Usage:
    # Compare a tilted stable SDE run against a Gaussian SDE run
    python evaluation/compare_models.py \\
        --eval-dirs \\
            evaluation/results/tilted_stable_sde/alpha_1.20_obsstd_0.10/data_16241891_train_29699958_gt_observations_obs_all_sim_training \\
            evaluation/results/gaussian_sde/alpha_1.20_obsstd_0.10/data_16241891_train_18187485_gt_observations_obs_all_sim_training

    # Same, but write output to a custom directory
    python evaluation/compare_models.py \\
        --eval-dirs \\
            evaluation/results/tilted_stable_sde/alpha_1.20_obsstd_0.10/data_16241891_train_29699958_gt_observations_obs_all_sim_training \\
            evaluation/results/gaussian_sde/alpha_1.20_obsstd_0.10/data_16241891_train_18187485_gt_observations_obs_all_sim_training \\
        --output-dir tilt_vs_gauss
"""

import sys
