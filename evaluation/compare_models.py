# =============================================================================

def parse_arguments() -> argparse.Namespace:
    """Parse command line arguments."""
    parser = argparse.ArgumentParser(
        description="Compare multiple trained SDE models",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__
    )

    parser.add_argument(
        '--eval-dirs',
        nargs='+',
        required=True,
        help='Evaluation result directories to compare'
    )

    # Output
    parser.add_argument(
        '--output-dir',
        type=str,
        default=None,
        help='Output directory for comparison results (default: auto-generated by joining eval dir names)'
    )

    parser.add_argument(
        '--comparisons-base',
        type=str,
        default='evaluation/results/comparisons',
        help='Base directory for comparison outputs (default: evaluation/results/comparisons)'
    )

    return parser.parse_args()


# =============================================================================
# RESULT LOADING
# =============================================================================

def load_evaluation_results(eval_dir: Path):
    """
    Load evaluation results from directory.

    Parameters
    ----------
    eval_dir : Path
        Evaluation results directory

    Returns
    -------
    results : EvaluationResults
        Loaded evaluation results
    summary : Dict
        JSON summary
    """
    # Load pickle details
    details_path = eval_dir / 'evaluation_details.pkl'
    if not details_path.exists():
        raise FileNotFoundError(f"Evaluation details not found: {details_path}")

    with open(details_path, 'rb') as f:
        results = pickle.load(f)

    # Load JSON summary
    summary_path = eval_dir / 'evaluation_summary.json'
    if summary_path.exists():
        with open(summary_path, 'r') as f:
            summary = json.load(f)
    else:
        summary = None

    return results, summary


