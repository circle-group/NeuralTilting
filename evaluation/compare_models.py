def main():
    """Main entry point."""
    args = parse_arguments()

    print(f"\n{'='*80}")
    print("MODEL COMPARISON")
    print(f"{'='*80}\n")

    # Step 1: Determine evaluation directories
    print("Step 1: Loading evaluation results...")
    eval_dirs = [Path(d) for d in args.eval_dirs]
    print(f"  Found {len(eval_dirs)} evaluation results")

    # Step 2: Load results
    print("\nStep 2: Loading evaluation data...")
    results_list = []
    summaries_list = []

    for eval_dir in eval_dirs:
        print(f"  Loading: {eval_dir}")
        results, summary = load_evaluation_results(eval_dir)
        results_list.append(results)
        summaries_list.append(summary)

    # Step 3: Create output directory
    if args.output_dir is None:
        # Auto-generate output directory name by joining the evaluation directory names
        output_name = '__'.join([Path(d).name for d in eval_dirs])
        output_dir = Path(args.comparisons_base) / output_name
    else:
        # If output_dir is a relative path without 'evaluation', prepend comparisons base
        output_dir = Path(args.output_dir)
        if not output_dir.is_absolute() and not str(output_dir).startswith('evaluation'):
            output_dir = Path(args.comparisons_base) / output_dir

    output_dir.mkdir(parents=True, exist_ok=True)
    print(f"\nOutput directory: {output_dir}")

    # Step 4: Create comparisons
    print("\nStep 3: Creating comparison visualizations...")

    plot_posterior_comparison(results_list, output_dir)
    create_metric_comparison_table(results_list, summaries_list, output_dir)
    plot_drift_comparison(results_list, output_dir)
    create_comparison_metadata(results_list, args, output_dir)

    print(f"\n{'='*80}")
    print("COMPARISON COMPLETE")
    print(f"{'='*80}\n")


if __name__ == '__main__':
    main()
