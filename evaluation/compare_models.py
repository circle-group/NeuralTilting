def plot_posterior_comparison(results_list: List, output_dir: Path):
    """
    Create side-by-side posterior plots with enhanced visualization.

    Shows:
    - All observations (training + held-out) with training emphasized
    - Actual posterior sample paths
    - Quantile uncertainty bands
    - True latent path

    Parameters
    ----------
    results_list : List[EvaluationResults]
        List of evaluation results
    output_dir : Path
        Output directory
    """
    n_models = len(results_list)

    # Load dataset and observations for first model (should be same for all)
    print("  Loading dataset for visualization...")
    dataset = load_dataset_for_run(results_list[0].run_config)
    obs_times = dataset['time_sequence']
    observations = dataset['observations']
    latent_path_full = dataset.get('latent_path')
    state_dim = observations.shape[1]

    # Recreate training/held-out split
    training_indices = recreate_observation_subsampling(obs_times, results_list[0].run_config)
    all_indices = jnp.arange(len(obs_times))
    heldout_indices = jnp.setdiff1d(all_indices, training_indices)

    # Interpolate latent path onto simulation grid if available
    if latent_path_full is not None:
        time_seq = results_list[0].time_sequence
        latent_path = np.stack([
            np.interp(time_seq, obs_times, latent_path_full[:, dim])
            for dim in range(state_dim)
        ], axis=1)
    else:
        latent_path = None

    # Create figure
    fig, axes = plt.subplots(
        nrows=state_dim,
        ncols=n_models,
        figsize=(10 * n_models, 6 * state_dim),
        squeeze=False
    )
    fig.patch.set_facecolor('white')

    # Pre-compute shared y-axis limits per dimension across all models.
    # Plots in the same row (same state dimension) share the same y-scale so
    # the two models are directly comparable; different rows are independent.
    shared_ylims = []
    for dim_idx in range(state_dim):
        all_lowers = []
        all_uppers = []
        for r in results_list:
            q = r.posterior_stats['quantiles']
            if '0.15%' in q and '99.85%' in q:
                all_lowers.append(q['0.15%'][:, dim_idx])
                all_uppers.append(q['99.85%'][:, dim_idx])
            else:
                m = r.posterior_stats['mean'][:, dim_idx]
                s = r.posterior_stats['std'][:, dim_idx]
                all_lowers.append(m - 3*s)
                all_uppers.append(m + 3*s)
        # Include observations and true latent so they are never clipped
        all_lowers.append(observations[:, dim_idx])
        all_uppers.append(observations[:, dim_idx])
        if latent_path is not None:
            all_lowers.append(latent_path[:, dim_idx])
            all_uppers.append(latent_path[:, dim_idx])
        global_min = float(np.min([np.min(a) for a in all_lowers]))
        global_max = float(np.max([np.max(a) for a in all_uppers]))
        padding = (global_max - global_min) * 0.05
        shared_ylims.append((global_min - padding, global_max + padding))

    # Plot each model
    for model_idx, results in enumerate(results_list):
        time_seq = results.time_sequence

        # Try to load posterior samples if saved
        posterior_samples = results.posterior_samples

        if posterior_samples is not None:
            print(f"    Model {model_idx} ({results.run_id}): Found posterior samples with shape {posterior_samples.shape}")
        else:
            print(f"    Model {model_idx} ({results.run_id}): No posterior samples found")

        for dim_idx in range(state_dim):
            ax = axes[dim_idx, model_idx]

            mean = results.posterior_stats['mean'][:, dim_idx]

            # Get quantiles (with backward compatibility)
            quantiles = results.posterior_stats['quantiles']
            if '0.15%' in quantiles and '99.85%' in quantiles:
                q_lower_997 = quantiles['0.15%'][:, dim_idx]
                q_upper_997 = quantiles['99.85%'][:, dim_idx]
                if model_idx == 0 and dim_idx == 0:
                    print(f"    Model {model_idx}: Using quantile-based 99.7% CI")
            else:
                # Fallback to std-based for old results
                std = results.posterior_stats['std'][:, dim_idx]
                q_lower_997 = mean - 3*std
                q_upper_997 = mean + 3*std
                if model_idx == 0 and dim_idx == 0:
                    print(f"    Model {model_idx}: Using std-based 99.7% CI (old results)")

            q_lower_95 = quantiles['2.5%'][:, dim_idx]
            q_upper_95 = quantiles['97.5%'][:, dim_idx]
            q_lower_68 = quantiles['16%'][:, dim_idx]
            q_upper_68 = quantiles['84%'][:, dim_idx]

            # Sanity check: ensure CIs are properly nested
            if model_idx == 0 and dim_idx == 0:
                width_997 = np.mean(q_upper_997 - q_lower_997)
                width_95 = np.mean(q_upper_95 - q_lower_95)
                width_68 = np.mean(q_upper_68 - q_lower_68)
                print(f"    Model {model_idx} CI widths: 99.7%={width_997:.3f}, 95%={width_95:.3f}, 68%={width_68:.3f}")

            # Layer 1: Individual posterior sample paths (if available)
            if posterior_samples is not None:
                n_samples_to_plot = min(30, posterior_samples.shape[0])
                sample_indices = np.linspace(0, posterior_samples.shape[0]-1, n_samples_to_plot, dtype=int)

                for i in sample_indices:
                    ax.plot(
                        time_seq,
                        posterior_samples[i, :, dim_idx],
                        color='#daa520',
                        alpha=0.35,
                        linewidth=0.9,
                        zorder=5
                    )

                if n_samples_to_plot > 0 and model_idx == 0 and dim_idx == 0:
                    ax.plot([], [], color='#daa520', alpha=0.6, linewidth=1.5, label='Posterior Samples')

            # Layer 2: 99.7% uncertainty band (quantile-based)
            ax.fill_between(
                time_seq,
                q_lower_997,
                q_upper_997,
                color='#d0d0d0',
                alpha=0.6,
                label='99.7% CI' if model_idx == 0 and dim_idx == 0 else None,
                zorder=2
            )

            # Layer 3: 2σ uncertainty band (95% CI)
            ax.fill_between(
                time_seq,
                q_lower_95,
                q_upper_95,
                color='#b0b0b0',
                alpha=0.7,
                label='95% CI' if model_idx == 0 and dim_idx == 0 else None,
                zorder=3
            )

            # Layer 4: 1σ uncertainty band (68% CI)
            ax.fill_between(
                time_seq,
                q_lower_68,
                q_upper_68,
                color='#808080',
                alpha=0.8,
                label='68% CI' if model_idx == 0 and dim_idx == 0 else None,
                zorder=4
            )

            # Layer 5: Held-out observations (all of them, smaller)
            if len(heldout_indices) > 0:
                ax.scatter(
                    obs_times[heldout_indices],
                    observations[heldout_indices, dim_idx],
                    marker='o',
                    color='#d62728',
                    s=8,
                    alpha=0.4,
                    label='Held-out Obs' if model_idx == 0 and dim_idx == 0 else None,
                    zorder=5
                )

            # Layer 6: True latent path
            if latent_path is not None:
                ax.plot(
                    time_seq,
                    latent_path[:, dim_idx],
                    '--',
                    color='#1f77b4',
                    linewidth=2.0,
                    alpha=1.0,
                    label='True Latent' if model_idx == 0 and dim_idx == 0 else None,
                    zorder=6
                )

            # Layer 7: Posterior mean
            ax.plot(
                time_seq,
                mean,
                color='black',
                linewidth=2.0,
                label='Posterior Mean' if model_idx == 0 and dim_idx == 0 else None,
                zorder=7
            )

            # Layer 8: Training observations (larger, emphasized)
            if len(training_indices) > 0:
                ax.scatter(
                    obs_times[training_indices],
                    observations[training_indices, dim_idx],
                    marker='o',
                    color='#ff7f0e',
                    s=25,
                    edgecolors='black',
                    linewidths=0.5,
                    label='Training Obs' if model_idx == 0 and dim_idx == 0 else None,
                    zorder=8
                )

            # Styling
            model_label = f"{results.model_type}"
            ax.set_title(f"Dimension {dim_idx} - {model_label}\n{results.run_id}",
                        fontsize=11, fontweight='bold')
            ax.set_xlabel("Time", fontsize=10)
            ax.set_ylabel("State", fontsize=10)
            ax.set_ylim(shared_ylims[dim_idx])  # shared scale per dimension
            ax.grid(True, alpha=0.3)
            ax.set_facecolor('#edebeb')

            if model_idx == 0 and dim_idx == 0:
                ax.legend(loc='best', fontsize=8, framealpha=0.9)

    plt.tight_layout()
    output_path = output_dir / 'posterior_comparison.png'
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()

    print(f"  Saved enhanced posterior comparison: {output_path}")


def create_metric_comparison_table(results_list: List, summaries_list: List, output_dir: Path):
    """
    Create metric comparison table as JSON.

    Parameters
    ----------
    results_list : List[EvaluationResults]
        List of evaluation results
    summaries_list : List[Dict]
        List of JSON summaries
    output_dir : Path
        Output directory
    """
    # Build comparison dictionary
    comparison = {
        'models': [
            {
                'run_id': r.run_id,
                'model_type': r.model_type
            }
            for r in results_list
        ],
        'metrics': {}
    }

    # Get all unique metric names
    all_metrics = set()
    for results in results_list:
        all_metrics.update(results.metrics.keys())

    # For each metric, collect values from all models
    for metric_name in sorted(all_metrics):
        comparison['metrics'][metric_name] = {}

        for results in results_list:
            value = results.metrics.get(metric_name)

            if value is None:
                comparison['metrics'][metric_name][results.run_id] = None
            elif isinstance(value, dict):
                # Drift errors (nested)
                comparison['metrics'][metric_name][results.run_id] = value
            elif isinstance(value, np.ndarray):
                if value.size == 1:
                    comparison['metrics'][metric_name][results.run_id] = float(value)
                else:
                    comparison['metrics'][metric_name][results.run_id] = "time-varying"
            else:
                comparison['metrics'][metric_name][results.run_id] = value

    # Save as JSON
    output_path = output_dir / 'metric_comparison.json'
    with open(output_path, 'w') as f:
        json.dump(comparison, f, indent=2)

    print(f"  Saved metric comparison: {output_path}")

    # Also print to console
    print("\n  Metric Comparison:")
    print("  " + "="*70)
    for metric_name, values_dict in comparison['metrics'].items():
        print(f"\n  {metric_name}:")
        for run_id, value in values_dict.items():
            model_type = next(m['model_type'] for m in comparison['models'] if m['run_id'] == run_id)
            if isinstance(value, dict):
                print(f"    {model_type} ({run_id}):")
                for k, v in value.items():
                    print(f"      {k}: {v}")
            else:
                print(f"    {model_type} ({run_id}): {value}")


def plot_drift_comparison(results_list: List, output_dir: Path):
    """
    Create drift parameter comparison plot using scatter plots with reference lines.

    Parameters
    ----------
    results_list : List[EvaluationResults]
        List of evaluation results
    output_dir : Path
        Output directory
    """
    # Check if drift_error metric exists for all models
    if not all('drift_error' in r.metrics and r.metrics['drift_error'] is not None for r in results_list):
        print("  Skipping drift comparison (not all models have drift_error metric)")
        return

    # Extract drift parameters
    model_labels = [f"{r.model_type}\n{r.run_id}" for r in results_list]
    drift_errors = [r.metrics['drift_error'] for r in results_list]

    # Determine drift type (OU, double well, or linear)
    first_error = drift_errors[0]
    if 'theta_error' in first_error:
        # OU drift
        param_names = ['theta', 'mu']
        param_labels = ['θ (Mean Reversion)', 'μ (Long-term Mean)']
    elif 'theta1_error' in first_error:
        # Double well drift: f(x) = theta1*x - theta2*x^3
        param_names = ['theta1', 'theta2']
        param_labels = ['θ₁ (Linear)', 'θ₂ (Cubic)']
    elif 'weight_error' in first_error:
        # Linear drift
        param_names = ['weight', 'bias']
        param_labels = ['Weight', 'Bias']
    else:
        print("  Unknown drift type, skipping drift comparison")
        return

    # Create scatter plot
    n_params = len(param_names)
    n_models = len(model_labels)
    # Compact height: scale with number of models, but keep it tight
    plot_height = max(3, 1.5 + 0.8 * n_models)
    fig, axes = plt.subplots(1, n_params, figsize=(7 * n_params, plot_height))
    if n_params == 1:
        axes = [axes]

    fig.patch.set_facecolor('white')

    for param_idx, (param_name, param_label) in enumerate(zip(param_names, param_labels)):
        ax = axes[param_idx]

        # Get true and trained values
        true_value = drift_errors[0][f'true_{param_name}']
        trained_values = [d[f'trained_{param_name}'] for d in drift_errors]
        errors = [d[f'{param_name}_error'] for d in drift_errors]

        # Create y positions for each model (closer spacing)
        y_positions = np.arange(len(model_labels)) * 0.4

        # Plot true value as vertical reference line
        ax.axvline(true_value, color='#2ca02c', linewidth=2.5,
                   linestyle='--', label='True Value', zorder=1, alpha=0.8)

        # Plot trained estimates as scatter points
        colors = ['#1f77b4', '#ff7f0e', '#d62728', '#9467bd', '#8c564b']
        for i, (y_pos, trained_val, error, label) in enumerate(zip(y_positions, trained_values, errors, model_labels)):
            color = colors[i % len(colors)]

            # Plot the estimate
            ax.scatter(trained_val, y_pos, s=150, color=color,
                      edgecolors='black', linewidths=1.5, zorder=3, alpha=0.9)

            # Draw line from true value to estimate (showing error)
            ax.plot([true_value, trained_val], [y_pos, y_pos],
                   color=color, linewidth=2, alpha=0.4, zorder=2)

            # Add error annotation
            if abs(error) > 1e-10:  # Only annotate if error is non-negligible
                error_text = f'Δ={error:.3g}'
                x_text = trained_val + (trained_val - true_value) * 0.1
                ax.text(x_text, y_pos, error_text,
                       fontsize=9, verticalalignment='center',
                       bbox=dict(boxstyle='round,pad=0.3', facecolor=color, alpha=0.2))

        # Styling
        ax.set_yticks(y_positions)
        ax.set_yticklabels(model_labels, fontsize=9)
        ax.set_xlabel(f'{param_label} Value', fontsize=11, fontweight='bold')
        ax.set_title(f'Drift Parameter: {param_label}', fontsize=12, fontweight='bold')
        ax.grid(True, alpha=0.3, axis='x')
        ax.set_facecolor('#f8f8f8')

        # Tighten y-axis limits to remove empty space
        y_margin = 0.3
        ax.set_ylim(-y_margin, y_positions[-1] + y_margin)

        # Add legend only to first subplot
        if param_idx == 0:
            ax.legend(loc='best', fontsize=10, framealpha=0.9)

        # Adjust x-limits to show all data clearly
        all_values = [true_value] + trained_values
        val_range = max(all_values) - min(all_values)
        if val_range > 0:
            margin = val_range * 0.2
            ax.set_xlim(min(all_values) - margin, max(all_values) + margin)
        else:
            # If all values are the same, add symmetric margins
            ax.set_xlim(true_value - abs(true_value)*0.1 - 0.1,
                       true_value + abs(true_value)*0.1 + 0.1)

    plt.tight_layout()
    output_path = output_dir / 'drift_comparison.png'
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()

    print(f"  Saved enhanced drift comparison: {output_path}")


def create_comparison_metadata(results_list: List, args: argparse.Namespace, output_dir: Path):
    """
    Create metadata file documenting which runs were compared.

    Parameters
    ----------
    results_list : List[EvaluationResults]
        List of evaluation results
    args : argparse.Namespace
        Command line arguments
    output_dir : Path
        Output directory
    """
    metadata = {
        'comparison_time': datetime.now().isoformat(),
        'n_models_compared': len(results_list),
        'runs_compared': [
            {
                'run_id': r.run_id,
                'model_type': r.model_type,
                'alpha': r.run_config['model'].get('alpha') or r.run_config['data'].get('alpha'),
                'obs_std': r.run_config['data']['obs_std'],
                'data_seed': r.run_config['data']['data_seed']
            }
            for r in results_list
        ]
    }

    output_path = output_dir / 'comparison_metadata.json'
    with open(output_path, 'w') as f:
        json.dump(metadata, f, indent=2)

    print(f"  Saved comparison metadata: {output_path}")


# =============================================================================
# MAIN
